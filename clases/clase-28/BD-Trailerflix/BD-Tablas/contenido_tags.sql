SELECT 
    contenidos.titulo AS Contenido,
    tags.tag AS Tag
FROM 
    Contenido_Tags 
JOIN 
    Contenidos ON contenido_tags.contenidoID = contenidos.contenidoID
JOIN 
    Tags ON contenido_tags.tagID = tags.tagID
ORDER BY 
    contenidos.contenidoID, tags.tag;

